# NumPy is a library to use pure arrays on Python, similar to Java Arrays

# Python does not implement arrays, just lists
# NumPy pure arrays are 50 times faster than Python lists

# Arrays are called ndarray on Numpy
# ndarray provides lots of supporting functions to work with them

# NumPy is faster than Python list because:
# ndarrays are stored at one continuous place in memory unlike Python lists.
# That makes their accessing and manipulating very efficient, it is called locality

# A large part of NumPy is written in C or C++


