# ------------------------------------------------------------------------------------------------------
# while loop
# ------------------------------------------------------------------------------------------------------

# structure: while condition
i = 0
while i < 6:
    i += 1
    if i == 1:
        continue
    if i == 4:
        break
    print(i)


# ------------------------------------------------------------------------------------------------------
# for loop
# ------------------------------------------------------------------------------------------------------

# for loop with a range, number of times
# initial, last, increment
for x in range(2, 6, 2):
    print("Number ")
    print(x)


